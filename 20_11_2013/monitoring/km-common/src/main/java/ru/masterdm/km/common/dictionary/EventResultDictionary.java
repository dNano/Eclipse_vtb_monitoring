package ru.masterdm.km.common.dictionary;

/**
 * Результат исполнения для мероприятий.
 * 
 * @author Shafigullin Ildar
 * 
 */
public enum EventResultDictionary {
	OK(1L), REMARK(2L), SANCTION(3L), FKR(4L), SANCTION_FKR(5L), CANCEL(6L), STOP(7L);

	private final long id;

	private EventResultDictionary(long id) {
		this.id = id;
	}

	public long getId() {
		return id;
	}
}
